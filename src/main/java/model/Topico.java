package model;

import java.util.List;

/**
 *
 * @author Henri
 */
public class Topico {

    private String titulo;
    private String autor;
    private String texto;
    private List<Comentarios> listaComentarios;

    public Topico(String titulo, String autor, String texto, List<Comentarios> listaComentarios) {
        this.titulo = titulo;
        this.autor = autor;
        this.texto = texto;
        this.listaComentarios = listaComentarios;
    }

    public String getTitulo() {
        return titulo;
    }

    public void setTitulo(String titulo) {
        this.titulo = titulo;
    }

    public String getAutor() {
        return autor;
    }

    public void setAutor(String autor) {
        this.autor = autor;
    }

    public String getTexto() {
        return texto;
    }

    public void setTexto(String texto) {
        this.texto = texto;
    }

    public List<Comentarios> getListaComentarios() {
        return listaComentarios;
    }

    public void setListaComentarios(List<Comentarios> listaComentarios) {
        this.listaComentarios = listaComentarios;
    }

}
